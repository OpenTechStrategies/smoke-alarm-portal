db.activeRegion.findOrCreate({
  where: {rc_region: "MOAK"},
  defaults: {
    region_name: "Missouri and Arkansas",
    is_active: true,
    internal_codes: "25R16"
}}).then();

regions.forEach(region => {
  db.activeRegion.findAll({
    where: {rc_region: region.region_code}
  }).then(region_list => {
    region_list.forEach(db_region => {
      db_region.region_name = region.name;
      db_region.save();
    })
  });
});

chapters.forEach(chapter => {
  db.chapter.findOrCreate({
    where: {code: chapter.code},
    defaults: {
      name: chapter.name,
      region: chapter.region_code
    }}).then();
});

counties.forEach(county => {
  county_no_spaces = county.county.replace(" ", "");
  db.SelectedCounties.findAll({
    where: {
      $or: [
        {county: { $ilike: county.county }},
        {county: { $ilike: county_no_spaces }},
      ],
      state: { $ilike: county.state }
    }
  }).then(matched_counties => {
    if(matched_counties.length == 0) {
      console.log("County " + county.county + ", " + county.state + " didn't have an entry in the database, creating");
      return db.SelectedCounties.create({
        county: county.county,
        state: county.state,
        chapter_code: county.chapter_code
      });
    } else if (matched_counties.length > 1) {
      console.log("County " + county.county + ", " + county.state + " had more than one entry in the database");
    } else {
      var db_county = matched_counties[0];
      db_county.chapter_code = county.chapter_code;
      return db_county.save()
    }
  }).then();
});
